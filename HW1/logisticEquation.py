import numpy as np

# x_{n+1} = rho * x_n * (1 - x_n)
# Starting value
x1 = 0.5

# rho values to iterate
rho_values = [0.8, 1.5, 2.8, 3.2, 3.5, 3.65]

# Number of iterations
n = 50

# Each column will store x(0) to x(49) for one rho value
results = np.zeros((n, len(rho_values)))

for col, rho in enumerate(rho_values):
    results[0, col] = x1
    for i in range(1, n):
        results[i, col] = rho * results[i - 1, col] * (1 - results[i - 1, col])

# Print results for each rho value
for col, rho in enumerate(rho_values):
    print(f"rho = {rho}")
    print(results[:, col])
    print()


